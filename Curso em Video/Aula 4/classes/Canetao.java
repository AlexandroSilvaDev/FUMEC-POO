package classes;

public class Canetao {

	private String modelo;
	private double ponta;
	
	public Canetao()
	{
		setModelo(modelo);
		setponta(ponta);
	}
	
	public String getModelo()
	{
		return modelo;
	}
	
	public void setModelo( String modelo )
	{
		this.modelo = modelo;
	}
	
	public double getPonta()
	{
		return ponta;
	}
	
	public void setponta( double ponta )
	{
		this.ponta = ponta;
	}
}
